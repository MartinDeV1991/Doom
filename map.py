levels = [level1, level2, level3]
num_enemies = [2, 5, 10]

class Map:
    def __init__(self, game, level=0):
        self.game = game
        self.levels = levels
        self.mini_map = self.levels[level]
        self.num_enemies = num_enemies[level]
        self.world_map = {}
        self.rows = len(self.mini_map)
        self.cols = len(self.mini_map[0])
        self.get_map()
        self.block_size = 100
        self.world_map_screen = pg.Surface((self.cols * self.block_size, self.rows * self.block_size), pg.SRCALPHA)

    def get_map(self):
        for j, row in enumerate(self.mini_map):
            for i, value in enumerate(row):
                if value:
                    self.world_map[(i, j)] = value

    def draw(self):
        [pg.draw.rect(self.game.screen, 'darkgray', (pos[0] * 100, pos[1] * 100, 100, 100), 2)
         for pos in self.world_map]               
    
    def draw_minimap(self):
        [pg.draw.rect(self.world_map_screen, 'darkgray', (pos[0] * 100, pos[1] * 100, 100, 100), 2)
         for pos in self.world_map]
                    
